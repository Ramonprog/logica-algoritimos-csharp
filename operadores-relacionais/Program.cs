int number1 = 8;
int number2 = 3;

// maior
Console.WriteLine(number1 > number2);

// maior ou igual
Console.WriteLine(number1 >= number2);

// menor
Console.WriteLine(number1 < number2);

// menor ou igual
Console.WriteLine(number1 <= number2);

// igual
Console.WriteLine(number1 == number2);

// diferente
Console.WriteLine(number1 != number2);